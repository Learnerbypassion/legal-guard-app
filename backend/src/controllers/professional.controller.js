const User = require("../models/user.model");
const Document = require("../models/document.model");
const { sendProfessionalContactEmail } = require("../services/email.service");
const PDFDocument = require("pdfkit");

/**
 * GET /api/professionals/recommend?type=Lawyer
 * Get a list of recommended professionals based on type
 */
const recommendProfessionals = async (req, res) => {
  try {
    const { type } = req.query; // e.g., "Lawyer", "CA"

    if (!type) {
      return res.status(400).json({
        success: false,
        error: "Professional type is required (e.g., Lawyer, CA)",
      });
    }

    const professionals = await User.find({
      role: "professional",
      "professionalDetails.profession": type,
    }).select("name email phone professionalDetails");

    res.status(200).json({
      success: true,
      data: professionals,
    });
  } catch (error) {
    console.error("❌ Recommend professionals error:", error.message);
    res.status(500).json({
      success: false,
      error: "Failed to fetch recommended professionals",
    });
  }
};

/**
 * Generate PDF from document analysis
 * @param {Object} document - Document with analysis data
 * @returns {Promise<Buffer>} PDF buffer
 */
const generateAnalysisPDF = async (document) => {
  return new Promise((resolve, reject) => {
    try {
      const doc = new PDFDocument({ margin: 50 });
      const chunks = [];

      doc.on("data", (chunk) => {
        chunks.push(chunk);
      });

      doc.on("end", () => {
        resolve(Buffer.concat(chunks));
      });

      doc.on("error", reject);

      // Title
      doc.fontSize(20).font("Helvetica-Bold").text("Legal Document Analysis", { align: "center" });
      doc.moveDown(0.5);
      doc.fontSize(10).font("Helvetica").text(`Generated by Legal Guardian`, { align: "center" });
      doc.moveDown(1);

      // Document Info
      doc.fontSize(12).font("Helvetica-Bold").text("Document Information");
      doc.fontSize(10).font("Helvetica");
      doc.text(`Filename: ${document.filename}`);
      doc.text(`Type: ${document.contractType}`);
      doc.text(`Risk Score: ${document.riskScore?.label || "Not assessed"}`);
      doc.moveDown(1);

      // Summary
      if (document.summary && document.summary.length > 0) {
        doc.fontSize(12).font("Helvetica-Bold").text("Summary");
        doc.fontSize(10).font("Helvetica");
        document.summary.forEach((point) => {
          doc.text(`• ${point}`);
        });
        doc.moveDown(1);
      }

      // Pros
      if (document.pros && document.pros.length > 0) {
        doc.fontSize(12).font("Helvetica-Bold").text("Advantages (Pros)");
        doc.fontSize(10).font("Helvetica");
        document.pros.forEach((pro) => {
          doc.text(`✓ ${pro.clause}: ${pro.explanation}`);
        });
        doc.moveDown(1);
      }

      // Cons
      if (document.cons && document.cons.length > 0) {
        doc.fontSize(12).font("Helvetica-Bold").text("Concerns (Cons)");
        doc.fontSize(10).font("Helvetica");
        document.cons.forEach((con) => {
          doc.text(
            `✗ [${con.severity?.toUpperCase()}] ${con.clause}: ${con.explanation} - ${con.advice}`
          );
        });
        doc.moveDown(1);
      }

      // Overall Advice
      if (document.overallAdvice) {
        doc.fontSize(12).font("Helvetica-Bold").text("Overall Advice");
        doc.fontSize(10).font("Helvetica");
        doc.text(document.overallAdvice, { align: "left", width: 400 });
        doc.moveDown(1);
      }

      // Risk Score Details
      if (document.riskScore) {
        doc.fontSize(12).font("Helvetica-Bold").text("Risk Assessment");
        doc.fontSize(10).font("Helvetica");
        doc.text(`Risk Level: ${document.riskScore.label}`);
        doc.text(`Score: ${document.riskScore.score}/100`);
        if (document.riskScore.description) {
          doc.text(`Details: ${document.riskScore.description}`);
        }
      }

      doc.end();
    } catch (error) {
      reject(error);
    }
  });
};

/**
 * POST /api/professionals/contact
 * Body: { professionalId, documentId }
 * Send an email to the professional on behalf of the current user with attached document
 */
const contactProfessional = async (req, res) => {
  try {
    const userId = req.userId;
    const { professionalId, documentId } = req.body;

    if (!professionalId) {
      return res.status(400).json({
        success: false,
        error: "Professional ID is required",
      });
    }

    const currentUser = await User.findById(userId);
    const professional = await User.findById(professionalId);

    if (!professional || professional.role !== "professional") {
      return res.status(404).json({
        success: false,
        error: "Professional not found",
      });
    }

    let pdfBuffer = null;
    let pdfFileName = "document.pdf";

    // If documentId is provided, generate PDF from the analysis
    if (documentId) {
      try {
        const document = await Document.findById(documentId);
        if (document) {
          pdfBuffer = await generateAnalysisPDF(document);
          pdfFileName = `${document.filename.replace(/\.[^/.]+$/, "")}_analysis.pdf`;
        }
      } catch (docError) {
        console.warn("⚠️  Could not fetch document for PDF:", docError.message);
        // Continue without PDF if document fetch fails
      }
    }

    // Send email to professional with PDF attachment
    await sendProfessionalContactEmail(
      professional.email,
      professional.name,
      currentUser.email,
      currentUser.name,
      pdfBuffer,
      pdfFileName
    );

    res.status(200).json({
      success: true,
      message: "Email sent successfully to the professional.",
      hasPDF: !!pdfBuffer,
    });
  } catch (error) {
    console.error("❌ Contact professional error:", error.message);
    res.status(500).json({
      success: false,
      error: "Failed to send email to the professional",
    });
  }
};

module.exports = {
  recommendProfessionals,
  contactProfessional,
};
